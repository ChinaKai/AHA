from __future__ import annotations

from pathlib import Path
import unittest

from aha_cli.web.http_utils import static_response


def static_root() -> Path:
    return Path(__file__).resolve().parents[1] / "src" / "aha_cli" / "web" / "static"


def app_entry_script(root: Path | None = None) -> str:
    static = root or static_root()
    return "\n".join(
        [
            (static / "app_runtime_setup.js").read_text(encoding="utf-8"),
            (static / "app_runtime_wiring.js").read_text(encoding="utf-8"),
            (static / "app.js").read_text(encoding="utf-8"),
        ]
    )


